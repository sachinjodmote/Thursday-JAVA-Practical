// Write a java program to find if the number is odd or even

class Even  //class declaration
{
	 public static void main(String args[])   //main method
	 {
		  int a = 33; // value initialization
		  
		  if(a%2==0)    // if condition declaration
		  {
			  System.out.println("The number is even");    // condition statement
		  }
		  else     // else condition declaration
		  {
			  System.out.println("The number is odd");    // condition statement
		  }
	 }
}
